package com.biomed.bm_access_layer.documentalmanager.domain.common;

/**
 * The EnumFinalDisposition enum represents the final disposition options for a
 * document.
 *
 * The enum defines four constants:
 * - TOTAL_CONSERVATION: Represents the option for total conservation of the
 * document.
 * - ELIMINATION: Represents the option for elimination of the document.
 * - MICROFILMING: Represents the option for microfilming the document.
 * - SELECTION: Represents the option for selecting a specific disposition for
 * the document.
 */
public enum EnumFinalDisposition {
    TOTAL_CONSERVATION,
    ELIMINATION,
    MICROFILMING,
    SELECTION;
}
