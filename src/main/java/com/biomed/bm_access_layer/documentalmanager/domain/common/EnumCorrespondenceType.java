package com.biomed.bm_access_layer.documentalmanager.domain.common;

/**
 * The EnumCorrespondenceType enum represents the type of correspondence
 * associated with a document.
 * It is used to differentiate between internal and external correspondence.
 *
 * The enum has two constants:
 * - INTERN: Represents internal correspondence.
 * - EXTERN: Represents external correspondence.
 */

public enum EnumCorrespondenceType {
    INTERN, EXTERN;
}
