package com.biomed.bm_access_layer.documentalmanager.domain.common;

/**
 * The EnumSupportType enum represents the support types for a document.
 *
 * The enum defines two constants:
 * - FISICAL: Represents the option for a document supported by FISICAL.
 * - ELECTRONIC: Represents the option for a document supported electronically.
 */
public enum EnumSupportType {
    FISICAL,
    ELECTRONIC;
}
